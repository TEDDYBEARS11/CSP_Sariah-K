#include <stdio.h>
int main(void){
//Loops are used to execute a block of code multiple times. There are three types of loops in C: for, while, and do-while.
//while loop
int start = 0;
while(start < 10){
    printf("Hello, World!\n");
    start = start + 1;
    start++;
}
//for loop
for(int i = 0; i < 10; i++){
    printf("Hello, World!");
}
}
